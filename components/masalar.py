import tkinter as tk
import functions.masaIcerikOlustur as tc
from tkinter.font import Font


# Masa #1 
def masa1():
    masa1Window = tk.Tk()
    masa1Window.title("Bergama MYO | Masa #1")
    masa1Window.geometry("320x480+100+50")
    masa1Window.resizable(False, False)

    header = Font(family='Arial', 
              size=14, 
              weight='normal')

    masaNo = tk.Label(masa1Window, text="İşlem Yaptığınız Masa : Masa #1", font=header)
    masaNo.pack()
    
    tc.icerikOlustur("masa1", masa1Window)


